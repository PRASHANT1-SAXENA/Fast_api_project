class Animal:
    def sound(self):
        print("Animal makes a sound")

    def move(self):
        print("Animal moves")


class Dog(Animal):
    def sound(self):
        print("Dog barks")

    def move(self):
        print("Dog runs")


class Bird(Animal):
    def sound(self):
        print("Bird chirps")

    def move(self):
        print("Bird flies")





